package LTSEDU_JavaBackend.src.A1_Java_Developer.J1_JavaCore.JA1_4;

public class JA1_4_02_EvenNumberPrinter {
    public static void main(String[] args) {
        int a = 100;

        for ( int i = 0; i < a; i++){
            if (i % 2 == 0){
                System.out.println(i);
            }
        }


    }
}
