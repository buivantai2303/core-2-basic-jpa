package LTSEDU_JavaBackend.src.A1_Java_Developer.J1_JavaCore.JA1_1;

public class JA1_1_01_BasicDeclareDataType {
    public static void main(String[] args) {



        int a = 5, b = 6, c = 7;
        double  d = 3.14f, e = 4.76f, f = 7.34f;
        char g = 'g', h = 'h', i = 'i';
        String str1 = "First string", str2 = "Second string", str3 = "Third string";
        System.out.println();

        System.out.print("INT TYPE: ");
        System.out.println(a + " " + b + " " + c);
        System.out.println();

        System.out.print("DOUBLE TYPE: ");
        System.out.println(d + " " + e + " " + f);
        System.out.println();

        System.out.print("CHAR TYPE: ");
        System.out.println(g + " " + h + " " + i);
        System.out.println();

        System.out.println("STRING TYPE");
        System.out.println("String 1: " + str1);
        System.out.println("String 2: " + str2);
        System.out.println("String 3: " + str3);

    }
}

